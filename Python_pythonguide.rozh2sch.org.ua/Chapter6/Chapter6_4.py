"""4. Зчитайте ціле число введене користувачем і виведіть повідомлення про те, чи число парне або непарне"""

while True:
    i = input('Введіть число(q для виходу): ')
    if i == 'q': 
        exit()
    elif not i.isnumeric():
        print('Це не число!')
    elif not int(i) % 2:
        print('Число парне')
    else:
        print('Число непарне')
