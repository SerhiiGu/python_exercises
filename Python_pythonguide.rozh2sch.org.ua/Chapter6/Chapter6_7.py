"""7. Зчитайте зі стандартного вводу цілі числа, по одному числу у рядку, \
і після першого введеного нуля виведіть суму отриманих на вхід чисел"""

i = 1
lst = []
while i != '0':
    i = input('Введіть число(0 для виходу):')
    if not i.isnumeric():
        print('Not number!')
    else:
        lst.append(int(i))
print('Список: ', lst)
print('Сума: ', sum(lst))
